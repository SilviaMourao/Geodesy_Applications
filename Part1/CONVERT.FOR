      PROGRAM CONVERT
      IMPLICIT REAL*8 (A-H,O-Z)
	CHARACTER*30 NOME

	OPEN(1,FILE='D73-1a.TXT',STATUS='OLD')
	OPEN(2,FILE='ETRS89-1a.TXT',STATUS='OLD')
	OPEN(3,FILE='COMB119.TXT',STATUS='UNKNOWN')
	IEOF=1

C	 LEITURA DO CABEÇALHO DOS FICHEIROS DE ENTRADA
	READ(1,*)
	READ(2,*)

	DO WHILE(IEOF.EQ.1)
	  READ(1,10,END=99)NOME,IG1,M1,S1,IG2,M2,S2,H1
	  READ(2,10,END=99)NOME,IG3,M3,S3,IG4,M4,S4,H2
	  WRITE(3,'(A20)')NOME
	  WRITE(3,11)IG1,M1,S1,IG2,M2,S2,H1
	  WRITE(3,11)IG3,M3,S3,IG4,M4,S4,H2
	ENDDO
10    FORMAT(A29,2(2I5,F9.3),F10.2)
11    FORMAT(2(2I4,F9.3),F9.2)

99    DO 20 I=1,3
20	 CLOSE(I)
	END
